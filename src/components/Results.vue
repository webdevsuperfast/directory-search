<template>
  <ul>
    <li v-for="(message, index) in messageList" :item="message" :key="index">{{ message }}</li>
  </ul>
</template>

<script>
export default {
  name: "Results",
  props: {
    msg: {
      type: String
    }
  },
  data: function() {
    return {
      messageList: []
    };
  },
  watch: {
    msg: function() {
      this.messageList.push(this.msg);
    }
  }
};
</script>